# for loops.py
# perulangan for

for x in range(5):
    print(x)

# loop pada string
for x in "banana":
    print(x)

# break
for x in range (6):
    if x == 3:
        break
    print(x)

# continue
for x in range(6):
    if x == 3:
        continue
    print(x)

#else pada for loop
for x in range(3):
    print(x)
else:
    print("Loop selesai")

